<?php

/**
 * 有一个数组存储当前老师已经确定的上课时间，例如:
 * [['2019-05-04 09:00','2019-05-04 10:00'],['2019-05-04 19:00','2019-05-04 19:30']]
 * 一个数组存储排课时间列表：
 * [['2019-05-04 10:00','2019-05-04 11:00'],['2019-05-04 19:00','2019-05-04 20:00']]
 */


function compare($a, $b)
{
    $result = [];
    foreach ($a as $k => $value) {
        if ($value[1] <= $b[$k][0]) {
            $result[$k] = true;
        } else {
            $result[$k] = false;
        }
    }
    return $result;
}

$a = [['2019-05-04 09:00', '2019-05-04 10:00'], ['2019-05-04 19:00', '2019-05-04 19:30']];
$b = [['2019-05-04 10:00', '2019-05-04 11:00'], ['2019-05-04 19:00', '2019-05-04 20:00']];

var_dump(compare($a,$b));
/**
 * output:
array(2) {
[0]=>
bool(true)
[1]=>
bool(false)
}
 */